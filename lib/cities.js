export function citiesMatchingPrefix(query, limit = 14) {
  const q = query.trim().toLocaleLowerCase('uk-UA');
  if (q.length < 1) return [];
  return UKRAINIAN_CITIES.filter((city) =>
    city.toLocaleLowerCase('uk-UA').startsWith(q)
  ).slice(0, limit);
}
